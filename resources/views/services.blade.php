<script>
    document.addEventListener("alpine:init", () => {
    Alpine.data("videoModal", () => ({
        open: false,
        toggle() {
            this.open = true;
            this.$nextTick(() => {
                this.playVideo();
            });
        },
        close() {
            this.open = false;
            this.pauseVideo();
        },
        playVideo() {
            const video = document.querySelector("#videoPlayer");
            if (video) {
                video.play();
            }
        },
        pauseVideo() {
            const video = document.querySelector("#videoPlayer");
            if (video) {
                video.pause();
                video.currentTime = 0; // Reset video to start
            }
        }
    }));
});

